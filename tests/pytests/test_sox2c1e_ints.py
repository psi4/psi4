pytestmark = [pytest.mark.psi, pytest.mark.api, pytest.mark.quick]

@uusing("einsums")
def test_sox2c1e_integrals():
    psi4.set_options({
        'reference': 'cghf',
        'relativistic': 'x2c',
        'spin_orbit_coupling': True
    })

    mol = psi4.core.Molecule.from_string("0 1\nZn 0 0 0\nZn 0 0 2")
    b1 = psi4.core.BasisSet.build(mol, 'BASIS', 'cc-pvdz-dk')
    b2 = psi4.core.BasisSet.build(mol, 'BASIS_RELATIVISTIC', 'cc-pvdz-dk')
    wfn = psi4.core.Wavefunction(mol, b1)
    wfn.set_basisset('BASIS_RELATIVISTIC', b2)
    mints = psi4.core.MintsHelper(wfn)

    Hx, Hy, Hz = [1j*block_diag(*m.to_array()) for m in mints.so_x2c_spin_orbit()]
    S = block_diag(*mints.so_overlap().to_array())
    T = block_diag(*mints.so_kinetic().to_array())
    V = block_diag(*mints.so_potential().to_array())

    nbf = b1.nbf()
    H = numpy.zeros((2*nbf, 2*nbf), dtype=complex)
    H[:nbf, :nbf] = T + V + Hz
    H[nbf:, nbf:] = T + V - Hz
    H[:nbf, nbf:] = Hx + 1j*Hy
    H[nbf:, :nbf] = Hx - 1j*Hy

    assert numpy.allclose(H, H.conj().T)

    S_ = block_diag(S, S)
    e_core, _ = eigh(H, S_)
    assert numpy.allclose(e_core, ref_evals)

if __name__ == "__main__":
    test_sox2c1e_integrals()
