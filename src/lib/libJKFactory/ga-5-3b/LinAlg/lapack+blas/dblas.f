     $         .NOT.GAL_LSAME( UPLO , 'L' )      )THEN
         INFO = 1
      ELSE IF( .NOT.GAL_LSAME( TRANS, 'N' ).AND.
     $         .NOT.GAL_LSAME( TRANS, 'T' ).AND.
